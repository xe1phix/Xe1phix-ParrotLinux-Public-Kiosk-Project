## ----------------------------------------------- ##
##   [?] PMap - Provide Memory Map libraries
##              Loaded by a running process
## ----------------------------------------------- ##


## ------------------------------------------------------------------------------ ##
     pmap -d $PID                ## Show the device format
## ------------------------------------------------------------------------------ ##
     pmap --extended $PID        ## Show the extended format
## ------------------------------------------------------------------------------ ##
     pmap -x $(pgrep java)       ## Show memory maps of processes running java
## ------------------------------------------------------------------------------ ##
     pmap -X $PID                ## Show  even  more  details
## ------------------------------------------------------------------------------ ##
     pmap -XX $PID               ## Show everything the kernel provides
## ------------------------------------------------------------------------------ ##
     pmap --show-path $PID       ## Show full path in the mapping column
## ------------------------------------------------------------------------------ ##



